package de.msg.jbit7.migration.itnrw.stamm;

public class HiAntragsteller {

	private Long beihilfenr;
	private Long context;
	private Long beginnDatum;
	private Long wertId;
	private Long wertInt;
	private String wertChar;
	private String zeitstempel;
	
	
	public Long getBeihilfenr() {
		return beihilfenr;
	}
	public void setBeihilfenr(Long beihilfenr) {
		this.beihilfenr = beihilfenr;
	}
	public Long getContext() {
		return context;
	}
	public void setContext(Long context) {
		this.context = context;
	}
	public Long getBeginnDatum() {
		return beginnDatum;
	}
	public void setBeginnDatum(Long beginnDatum) {
		this.beginnDatum = beginnDatum;
	}
	public Long getWertId() {
		return wertId;
	}
	public void setWertId(Long wertId) {
		this.wertId = wertId;
	}
	public Long getWertInt() {
		return wertInt;
	}
	public void setWertInt(Long wertInt) {
		this.wertInt = wertInt;
	}
	public String getWertChar() {
		return wertChar;
	}
	public void setWertChar(String wertChar) {
		this.wertChar = wertChar;
	}
	public String getZeitstempel() {
		return zeitstempel;
	}
	public void setZeitstempel(String zeitstempel) {
		this.zeitstempel = zeitstempel;
	}
	

}
